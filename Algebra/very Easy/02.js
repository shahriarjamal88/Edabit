/*
Return the Next Number from the Integer Passed
Create a function that takes a number as an argument, increments the number by +1 and returns the result.

Examples
addition(0) ➞ 1

addition(9) ➞ 10

addition(-3) ➞ -2
*/
function additionNum(x){
   return ++x;
}
console.log(additionNum(0))
console.log(additionNum(9))
console.log(additionNum(-3))